package com.rittik.MyQuizzApp.dto;

public class UserOptionResponseDTO {
    public Long id;
    public String text;

    public UserOptionResponseDTO(Long id, String text) {
        this.id = id;
        this.text = text;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
}
